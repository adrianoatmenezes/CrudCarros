    private void tblListaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblListaMouseClicked
        int linhaPesquisar = tblLista.getSelectedRow();
        Estaticos.carro.setId((int) tblLista.getValueAt(linhaPesquisar, 0));
        Estaticos.carro.setFabricante((String) tblLista.getValueAt(linhaPesquisar, 1));
        Estaticos.carro.setModelo((String) tblLista.getValueAt(linhaPesquisar, 2));
        Estaticos.carro.setCor((String) tblLista.getValueAt(linhaPesquisar, 3));
        Estaticos.carro.setAno_fabricado((int) tblLista.getValueAt(linhaPesquisar, 4));
        Estaticos.carro.setValor((float) tblLista.getValueAt(linhaPesquisar, 5));
    }//GEN-LAST:event_tblListaMouseClicked

