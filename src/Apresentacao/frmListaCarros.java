    private void preencherTabela() {
        DefaultTableModel modelo = (DefaultTableModel) tblLista.getModel();
        modelo.setNumRows(0);
        for (Carros p : Estaticos.listaCarros) {
            modelo.addRow(new Object[]{
                p.getId(), p.getFabricante(), p.getModelo(), p.getCor(), p.getAno_fabricado(), p.getValor()
        });
    }
}

